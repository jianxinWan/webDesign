function changeHistoryInfo(){
    var i=0;
    $(".historyBlock .button").click(function(){
        i+=2;
        if(i==12){
            i=-2;
        }else{
            addHistory(i);
        }
    });
}
function addHistory(index){
    //设置动画
    setTimeout(function(){
        $(".historyBox1").addClass("animated flipInY");
        $(".historyBox2").addClass("animated slideInRight");
        $(".historyBox3").addClass("animated slideInLeft");
        $(".historyBox4").addClass("animated flipInY");
        setTimeout(function(){
            $(".button").addClass("animated fadeIn");
            $(".historyBox1").removeClass("animated flipInY");
            $(".historyBox2").removeClass("animated slideInRight");
            $(".historyBox3").removeClass("animated slideInLeft");
            $(".historyBox4").removeClass("animated flipInY");
        },1100);
    },0);
    $(".button").addClass("animated fadeIn");
    //添加照片信息
    $(".HistoryTopInfo .historyImg img").attr("src",HistoryArr[index].dynastyImgSrc);
    $(".HistoryButtomInfo .historyImg img").attr("src",HistoryArr[index+1].dynastyImgSrc);
    //添加标题
    $(".boxContent img").attr("src",HistoryArr[index].dynastyTitSrc);
    $("#historyBoxLeft img").attr("src",HistoryArr[index+1].dynastyTitSrc);
    //添加内容
    $(".historyInfoBox1").html(HistoryArr[index].dynastyInfo);
    $(".historyInfoBox2").html(HistoryArr[index+1].dynastyInfo);
}
changeHistoryInfo();
